let tooltip;
let selectedYear = null;
let lineX = 0;
let filterType = "All";
let searchQuery = "";
let yearRangeMin = 100;
let yearRangeMax = 2024;
let highlightedDesigner = null;
let isExpandedTooltipOpen = false; // Track if the expanded tooltip is open
let adirFont;

const totalVerticalLines = 140; // Number of vertical lines
const totalHorizontalLines = 25; // Number of horizontal lines
const tooltipWidth = 250; // Fixed width for tooltips


function setup() {
    createCanvas(windowWidth, 600);
    tooltip = document.querySelector('.tooltip');
    if (tooltip) {
        tooltip.style.visibility = 'hidden';
    }
    const expandedTooltip = document.querySelector('.expanded-tooltip');

    // Add mousedown event listener to toggle the expanded tooltip
    window.addEventListener('mousedown', (event) => {
        if (isExpandedTooltipOpen && !expandedTooltip.contains(event.target)) {
            hideExpandedTooltip();
        }
    });

    expandedTooltip.addEventListener('mousedown', (event) => {
        event.stopPropagation(); // Prevent closing when tapping inside the expanded tooltip
    });

    // Close button functionality
    const closeButton = document.getElementById('closeButton');
    if (closeButton) {
        closeButton.addEventListener('mousedown', (event) => {
            hideExpandedTooltip();
            event.stopPropagation(); // Prevent closing the tooltip when tapping the close button
        });
    }

    textAlign(CENTER, CENTER);
   
}

function draw() {
    background('#F04E30');
    displayGrid();
    displayYearIndicator();
    displayTimeline();
}

function displayGrid() {
    let verticalSpacing = width / (totalVerticalLines - 1);
    let horizontalSpacing = height / (totalHorizontalLines - 1);
    
    // Set properties for vertical lines
    stroke(255, 255, 255, 40); // Existing opacity for vertical lines
    strokeWeight(0.8);

    // Draw vertical lines
    for (let i = 0; i < totalVerticalLines; i++) {
        let x = i * verticalSpacing;
        line(x, 0, x, height);
    }

    // Set properties for horizontal lines with reduced opacity
    stroke(255, 255, 255, 10); // Reduced opacity for horizontal lines

    // Draw horizontal lines
    for (let j = 0; j < totalHorizontalLines; j++) {
        let y = j * horizontalSpacing;
        line(0, y, width, y);
    }
}


function displayYearIndicator() {
    let yearGroups = {};
    models.forEach((model) => {
        if (model.model.toLowerCase().includes(searchQuery.toLowerCase()) && model.year >= yearRangeMin && model.year <= yearRangeMax) {
            let year = model.year;
            if (!yearGroups[year]) {
                yearGroups[year] = [];
            }
            yearGroups[year].push(model);
        }
    });

    let yearsWithData = Object.keys(yearGroups).sort((a, b) => parseInt(a) - parseInt(b));
    let verticalSpacing = width / (totalVerticalLines - 1);
    let startX = 2 * verticalSpacing; // Offset to start from the 6th vertical line
    let nearestYear = null;
    let minDist = Infinity;

    yearsWithData.forEach((year, yearIndex) => {
        let x = startX + yearIndex * verticalSpacing;
        let distToMouse = abs(mouseX - x);

        if (distToMouse < minDist) {
            minDist = distToMouse;
            nearestYear = year;
            lineX = x;
        }
    });

    if (nearestYear !== null) {
        selectedYear = nearestYear;
    }

    stroke('#000000');
    strokeWeight(2);
    line(lineX, 0, lineX, height);

    if (selectedYear !== null) {
        fill(255);
        noStroke();
        textSize(24);
        
        // Check if the selected year text exceeds the 5th line from the right
        let positionToRight = lineX < (width - 7 * verticalSpacing);
        
        // Position text accordingly
        let textPositionX = positionToRight ? lineX + 10 : lineX - 20;
        let textAlignMode = positionToRight ? LEFT : RIGHT;
        
        textAlign(textAlignMode, CENTER);
        text(selectedYear, textPositionX, 20); 
    }
}


