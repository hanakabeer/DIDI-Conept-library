function displayTimeline() {
    let isHoveringDot = false; // Track if the mouse is over any dot
    let yearGroups = {};
    models.forEach((model) => {
        if (model.model.toLowerCase().includes(searchQuery) && model.year >= yearRangeMin && model.year <= yearRangeMax) {
            let year = model.year;
            if (!yearGroups[year]) {
                yearGroups[year] = [];
            }
            yearGroups[year].push(model);
        }
    });

    let yearsWithData = Object.keys(yearGroups).sort((a, b) => parseInt(a) - parseInt(b));
    let verticalSpacing = width / (totalVerticalLines - 1);
    let horizontalSpacing = height / (totalHorizontalLines - 1);

    let designerDots = [];
    let occupiedPositions = new Set(); // Set to track occupied positions

    yearsWithData.forEach((year, yearIndex) => {
        let group = yearGroups[year];
        let numModels = group.length;

        let baseX = (yearIndex + 2) * verticalSpacing;

        let initialY;
        if (yearIndex < 20) {
            initialY = height / 2 + (yearIndex % 2 === 0 ? -1 : 1) * (horizontalSpacing / 2);
        } else {
            initialY = height / 2 - (numModels - 1) * (horizontalSpacing / 2);
        }

        group.forEach((model, index) => {
            let y = initialY + index * horizontalSpacing;

            if (y < horizontalSpacing) y = horizontalSpacing;
            else if (y > height - horizontalSpacing) y = height - horizontalSpacing;

            while (occupiedPositions.has(`${baseX},${y}`)) {
                y += 10;
            }

            occupiedPositions.add(`${baseX},${y}`);

            let isSelected = (filterType === "All" || model.type === filterType);
            let opacity = isSelected ? 255 : 80;

            let distToMouse = dist(mouseX, mouseY, baseX, y);
            let maxDotSize = isSelected ? 14 : 10;
            let minDotSize = isSelected ? 7 : 5;
            let dotSize = lerp(maxDotSize, minDotSize, constrain(distToMouse / 160, 0, 1));

            let isHovered = distToMouse < dotSize / 2;

            // Change dot color to black when hovered
            fill(isHovered ? color(0, 0, 0) : color(255, 255, 255, opacity)); 

            noStroke();
            if (dotSize > 0) {
                ellipse(baseX, y, dotSize, dotSize);
            }

            if (isHovered && isSelected) {
                tooltip.style.visibility = 'visible';
                tooltip.innerHTML = `Model: ${model.model}<br>Designer: ${model.designer}`;
                positionTooltip(tooltip, baseX, y, tooltipWidth);

                highlightedDesigner = model.designer;

                stroke(0);
                noFill();
                strokeWeight(2);
                ellipse(baseX, y, dotSize + 14, dotSize + 14);
                

                if (mouseIsPressed && !isExpandedTooltipOpen) {
                    showExpandedTooltip(model, baseX, y);
                }

                isHoveringDot = true; // Set to true if hovering over any dot
            }

            if (isSelected && model.designer === highlightedDesigner && 
                model.designer && model.designer.toLowerCase() !== "unknown" && model.designer.toLowerCase() !== "various" ) {
                designerDots.push({ x: baseX, y });
            }
        });
    });

    if (designerDots.length > 1) {
        stroke(0, 0, 0, 255);
        strokeWeight(3);
        for (let i = 0; i < designerDots.length - 1; i++) {
            line(designerDots[i].x, designerDots[i].y, designerDots[i + 1].x, designerDots[i + 1].y);
        }
    }

    if (highlightedDesigner) {
        designerDots.forEach(dot => {
            let pulse = (sin(frameCount * 0.1) + 1) * 0.1 + 1;
            fill(0);
            noStroke();
            ellipse(dot.x, dot.y, 14 * pulse, 14 * pulse);
        });
    }

    if (!designerDots.length) {
        tooltip.style.visibility = 'visible';
        highlightedDesigner = null;
    }

    // Hide the tooltip if not hovering any dot
    if (!isHoveringDot) {
        tooltip.style.visibility = 'hidden';
    }
}






function showExpandedTooltip(model, mouseX, mouseY) {
    const expandedTooltip = document.querySelector('.expanded-tooltip');
    const tooltip = document.querySelector('.tooltip');
    const p5Container = document.querySelector('.p5container');
    const containerRect = p5Container.getBoundingClientRect();
    const tooltipWidth = 400;
    const tooltipHeight = expandedTooltip ? expandedTooltip.offsetHeight : 0;

    // Calculate the 25th vertical line from the right edge
    const rightBoundary = containerRect.right;
    const line25thFromRight = rightBoundary - 60 * 10; // Assuming 10px spacing per line

    if (expandedTooltip) {
        // Hide the regular tooltip before showing the expanded one
        if (tooltip) {
            tooltip.style.visibility = 'hidden';
            tooltip.style.opacity = '0';
        }

        // Prepare the expanded tooltip
        expandedTooltip.style.visibility = 'visible';
        expandedTooltip.style.transition = 'opacity 0.3s ease, transform 0.3s ease';
        expandedTooltip.style.opacity = '1';

        let left, top = mouseY - tooltipHeight / 2;

        // Adjust tooltip to stay within 700px boundary from the top
        if (top + tooltipHeight > 500) {
            top = 500 - tooltipHeight;
        }
        if (top < containerRect.top) {
            top = containerRect.top;
        }

        // Position to the left if mouseX is within the 25th line to the right edge, otherwise to the right
        if (mouseX >= line25thFromRight) {
            left = mouseX - tooltipWidth - 70; // Position to the left
        } else {
            left = mouseX + 70; // Position to the right
        }

        expandedTooltip.style.left = `${left}px`;
        expandedTooltip.style.top = `${top}px`;
        expandedTooltip.style.width = `${tooltipWidth}px`;

        // Populate tooltip with model details
        expandedTooltip.querySelector('h2').innerText = model.model;
        expandedTooltip.querySelector('h3').innerText = model.designer;
        expandedTooltip.querySelector('.year').innerText = model.year;
        expandedTooltip.querySelector('.type').innerText = model.type;
        expandedTooltip.querySelector('.id').innerText = model.id;
        expandedTooltip.querySelector('.description').innerText = model.description;
        expandedTooltip.querySelector('.country').innerText = model.country;

       // const linkElement = expandedTooltip.querySelector('.link');
       // linkElement.innerText = model.link;
      //  linkElement.href = model.link;
      //, '.link'

        const imageElement = expandedTooltip.querySelector('.model-image');
        imageElement.src = model.image;

        // Hide elements if their content is empty
        ['h2', 'h3', '.year', '.type', '.id', '.description', '.country'].forEach(selector => {
            const element = expandedTooltip.querySelector(selector);
            element.style.display = element.innerText.trim() ? 'block' : 'none';
        });

        // Hide image container if the image URL is empty
        imageElement.parentElement.style.display = model.image ? 'block' : 'none';

        isExpandedTooltipOpen = true;
        noLoop(); // Stop the draw loop when the expanded tooltip is open
    }
}

function hideExpandedTooltip() {
    const expandedTooltip = document.querySelector('.expanded-tooltip');
    if (expandedTooltip) {
        expandedTooltip.style.opacity = '0';
        expandedTooltip.style.transform = 'scale(0.95'; // Optional scale effect

        setTimeout(() => {
            expandedTooltip.style.visibility = 'hidden'; // Set visibility after transition
            expandedTooltip.style.transform = 'scale(1)'; // Reset scale for next show

            // Show the regular tooltip only if the expanded tooltip is hidden
            const tooltip = document.querySelector('.tooltip');
            if (tooltip) {
                tooltip.style.visibility = 'visible'; // Show regular tooltip
                tooltip.style.opacity = '1'; // Fade in regular tooltip
            }

            isExpandedTooltipOpen = false;
            loop(); // Resume the draw loop
        }, 300); // Delay matches transition duration
    }
}


// Function to position the tooltips
function positionTooltip(tooltipElement, x, y, width) {
    tooltipElement.style.width = width + 'px'; // Set consistent width
    let left = x + 20; // Default position to the right of the dot

    // Check if there is enough space on the right side of the screen
    if (left + width > windowWidth) {
        left = x - width - 60; // Position on the left if not enough space
    }

    tooltipElement.style.left = left + 'px'; // Position the tooltip on the right side of the dot + 'px';
    tooltipElement.style.top = (y + 10) + 'px'; // Positioning slightly above the dot
}

function positionExpandedTooltip(tooltipElement, dotX, dotY) {
    const tooltipWidth = 400; // Set desired width
    tooltipElement.style.width = `${tooltipWidth}px`;

    // Get tooltip height after setting the width
    const tooltipHeight = tooltipElement.offsetHeight;

    // Get p5 container boundaries
    const p5Container = document.querySelector('.p5container');
    const containerRect = p5Container.getBoundingClientRect();

    // Calculate available space on each side of the dot
    const spaceOnLeft = dotX - containerRect.left;
    const spaceOnRight = containerRect.right - dotX;

    // Determine if the tooltip fits better on the left or right of the dot
    let left;
    if (spaceOnRight >= tooltipWidth + 10) {
        // Place tooltip to the right of the dot with a 10px margin
        left = dotX + 10;
    } else if (spaceOnLeft >= tooltipWidth + 10) {
        // Place tooltip to the left of the dot with a 10px margin
        left = dotX - tooltipWidth - 50;
    } else {
        // Default to placing tooltip on the right if space is limited on both sides
        left = dotX + 10;
    }

    // Calculate vertical position to center tooltip around the dot
    let top = dotY - tooltipHeight / 2;

    // Ensure the tooltip does not go above the container
    if (top < containerRect.top) {
        top = containerRect.top;
    }

    // Ensure the tooltip does not extend below the container
    if (top + tooltipHeight > containerRect.bottom) {
        top = containerRect.bottom - tooltipHeight;
    }

    // Apply calculated positions
    tooltipElement.style.left = `${left}px`;
    tooltipElement.style.top = `${top}px`;
    tooltipElement.style.position = 'absolute'; // Position it absolutely within the container
}













